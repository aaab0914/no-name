my_list = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h']

# 1. Get the first three elements
first_three = my_list[0:3]
print(first_three)

# 2. Get elements from index 2 to the end
from_index_two = my_list[2:]
print(from_index_two)

# 3. Get the last three elements
last_three = my_list[-3:]
print(last_three)

# 4. Get every second element
every_second = my_list[::2]
print(every_second)

# 5. Reverse the list using slicing
reversed_list = my_list[::-1]
print(reversed_list)
 
